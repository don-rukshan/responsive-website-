      <h2 class="pb-4">Get The Benefits Of Cardio In A Fraction Of The Time! </h2>
      <h5 class="pb-4"> Here's some good news if you're pressed for time! Research shows that very short, high-intensity workouts can actually deliver the same health benefits as much longer periods of moderate exercise.</h5>

      <div class="col-lg-12 pb-5"><img class="img-fluid" src="img/work4.png"></div>

      <p class="text-justify">Ask people what gets in the way of them exercising more, and a lot of them will say that they just don't have the time. But according to recent research, you might not need nearly as much time as you think to get in a seriously beneficial workout.</p>

      <p class="text-justify">In this study, 27 men who were considered sedentary were divided into three groups. One group did 50 minutes of moderate exercise three times a week, another group did 10 minutes of high-intensity exercise in the form of sprint interval training (SIT), and a third just kept being sedentary. The 10-minute SIT workout included a 2-minute warm-up and a 3-minute cool-down.[1]</p>


      <p class="text-justify">Here's how the short routine looked:
      </p>

      <ul class="text-justify"> <b>Cycling:</b>
       <li>
        Warm-up: 2 minutes</li>
       <li> Sprint: 20 seconds</li>
       <li>Low-to-moderate effort cycling: 2 minutes</li>
       <li>Sprint: 20 seconds</li>
       <li>Low-to-moderate effort cycling: 2 minutes</li>
       <li>Sprint: 20 seconds</li>
       <li>Low-to-moderate effort cycling: 2 minutes</li>
       <li>Cool-down: 2-3 minutes
       </li>
      </ul>

      <p class="text-justify"> For those of you counting at home, that's 10 minutes of total work, and just 1 minute—seriously!—of sprinting, done three times a week. That couldn't be enough to elicit any kind of serious response, right? </p>

      <div class="col-lg-12 pb-5"><img class="img-fluid" src="img/work44.png"></div>

      <p class="text-justify">Well, after 12 weeks, the researchers found that the results of a number of key health measurements were virtually the same for the people who did either the long bouts of moderate-intensity continuous training (MICT) or sprint interval training (SIT)—even though the men doing SIT had a five-fold lower exercise volume and time commitment. Researchers measured for insulin sensitivity, cardio respiratory fitness, and skeletal muscle strength.</p>

      <p class="text-justify">According to the Centers for Disease Control, adults should do at least 150 minutes a week of moderate exercise or 75 minutes a week of vigorous exercise, and the vigorous exercises should be done for at least 10 minutes at a time.[2] But when matched up against men doing 150 minutes of MICT, the group doing SIT needed only 30 minutes a week—and only 3 total minutes per week of high-intensity exercise—to get the same effect.</p>

      <p class="text-justify">The study also cites recent research that found people get more enjoyment from doing high-intensity workouts than from doing moderate-intensity ones—and are more likely to continue doing the exercise over a four-week period.</p>

      <p class="text-justify pb-5">Choosing to do moderate or intense exercise depends on a number of factors, including (of course) your goals. But if you want to save more time each week, fit your cardio into shorter time periods, or just don't like cardio and want to get it over with as quickly as possible, consider focusing on very short bursts of high-intensity workouts. You may just end up enjoying it more! </p>


