      <h2 class="pb-4">How To Add Muscle Without Adding Weight To The Bar </h2>
      <h5 class="pb-4"> Getting strong is cool, but when you want to build muscle, you can be more creative than just loading more plates and lugging heavier dumbbells.</h5>

      <div class="col-lg-12 pb-5"><img class="img-fluid" src="img/work2.png"></div>

      <p class="text-justify">Lifters are getting smarter. They know that it's not just about how big their bench press is; going heavy isn't the only way to make muscles grow. Being able to add weight to the bar is the byproduct of the gains you make from previous workouts—and from implementing other training strategies, like eccentric loading and isometrics.</p>

      <p class="text-justify"> Muscle hypertrophy is the process in which muscle fibers are broken down and repaired. It's an adaptive response to force generated against increased resistance. The recommended formula for stimulating hypertrophy is to lift submaximal weights for 6-12 repetitions per exercise. Strength development calls for heavier loads—usually around 80-90 percent of maximum—for sets of 2-5 repetitions, and anaerobic endurance requires lighter sets of 15 or more reps. </p>


